<p>Tout d'abord, félicitations pour l'admission de votre petit Astérix parce que vous y êtes sûrement pour quelque chose ! Mais ça y est, votre petit protégé est désormais un jeune Insalien et il sera bientôt temps pour lui de quitter le cocon familial pour la merveilleuse contrée de Lugdunum. C’est bien sûr pour notre petit Astérix un pas vers l’inconnu, mais nous sommes conscients que c’en est également un pour vous. Oui oui, c’est à vous que nous pensons, vous sa maman qui vous demandez comment votre enfant va pouvoir s’endormir sans les berceuses que vous lui chantiez tous les soirs. Tout comme lui est déjà nostalgique des bons petits plats de papa le dimanche midi !  </p>

