<img id="motParent_Idefix" src="content/img/motParent_Idefix.svg"></img>

<p>C’est en particulier son dévoué, attentionné et magnifique parrain ou marraine qui l’accueillera dès son arrivée à l’INSA. Il ou elle lui fera découvrir les vastes contrées du campus de La Doua afin qu’il s’y sente immédiatement à l’aise et sera présent pour le guider lors de ses premiers pas de jeune étudiant (pour ce qui est de la berceuse, il faudra en revanche surement s’habituer à faire sans, non pas par manque de volonté des parrains et marraines mais bien par problème d’éloignement entre les bâtiments des premières et deuxièmes années !). </p>                      

<p>Dans la foulée, notre petit Astérix fera connaissance de nombreux autres jeunes Astérix lors de l’intégration. Quelques précisions s’imposent peut-être par rapport à l’intégration et au nom « bizuths » donné à nos bien-aimés petits Astérix. Il n’est, lors de l’intégration absolument pas question de bizutages, pratiques sévèrement punies par la loi et très surveillées par le Bureau des Élèves. Ces pratiques n'ont jamais existé à l'INSA et n'auront jamais leur place dans notre école. De plus le surnom de « bizuth » attribué aux jeunes Astérix est un terme affectif bien plus sympa que le formel "primo-entrant".  </p>

<img id="motParent_Velo" src="content/img/motParent_Velo.svg"></img>

<p>L’intégration, comme son nom l’indique, est donc bien pour tous les jeunes Astérix la parfaite occasion de faire connaissance, passer de bons moments et tisser des liens avec leurs camarades de promo avec qui ils partageront les cinq belles années à venir, le tout dans une ambiance bon enfant ! </p>

<p>Pendant ces jours d’intégration votre enfant fera également connaissance de sa famille (cf. page suivante) à l’INSA. C’est avec sa famille qu’il partagera de grands repas le soir lors desquels bonne humeur et rire sont toujours au rendez-vous, ambiance bienvenue après huit longues heures de calculs d’entropie d'univers, de circulation de champ magnétique ou autres joyeuses occupations. </p> 

<p>Ainsi, bien que votre petit Astérix s'apprête à faire un grand saut dans l'inconnu, vous pouvez dormir sur vos deux oreilles. Nous serons là pour l'accueillir et l'accompagner au mieux pendant cette année pour que lui aussi puisse l'an prochain perpétuer la tradition et réserver un accueil aussi chaleureux aux petits Mickey (?) Harry Potter (?) Dora(?) de l'année prochaine... </p>
	
